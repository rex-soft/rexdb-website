<h2><div id="user-content-summary">概述</div></h2>

<p>Rexdb是一款使用Java语言编写的，开放源代码的持久层框架。提供了查询、调用、（JTA）事务、数据源管理等功能，可以取代Mybatis、Hibernate作为系统的核心ORM框架。</p>

<p>Rexdb提供了工具类风格的接口，不需要编写配置文件，使用简便；同时，它还具备同类框架中最高效的执行效率。</p>

